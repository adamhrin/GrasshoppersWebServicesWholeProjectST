package Models;

/**
 *
 * @author Adam
 */
public class Component {
    private int id;
    private String name;

    public int getId() {
        return this.id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public boolean equals(Object obj) {
        Component comp = (Component)obj;
        return this.id == comp.getId();
    }
    
    
}
